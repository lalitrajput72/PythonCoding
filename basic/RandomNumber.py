# We will use python lib random for this
import random

print("Random number between {0} and {1} is : {2}".format(random.randint(0,100), 0, 100))

# Print New line in python
# print() funtions takes you to new line every time you use it
print("Hello")
print("world")
# add extra line using print()
print("Hello")
print()
print("world")
# Print both in single line
# end at the end of a print adds next print statement to itself
print("Hello", end=" ")
print("world")
